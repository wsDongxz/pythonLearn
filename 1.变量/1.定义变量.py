# 定义一个列表类型的变量
list1 = []
list2 = list()

print("list1的数据类型：", type(list1))
print("list2的数据类型：", type(list2))


# 定义一个字典变量

dict1 = {}
dict2 = dict()
print("dict1的数据类型：", type(dict1))
print("dict2的数据类型：", type(dict2))

